class PlaceModel {
  final String id;
  final String name;
  final String icon;
  final double latitude;
  final double longitude;
  final String phoneNumber;
  final String address;

  const PlaceModel({
    required this.id,
    required this.name,
    required this.icon,
    required this.latitude,
    required this.longitude,
    required this.phoneNumber,
    required this.address,
  });
}
